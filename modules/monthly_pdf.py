def _compute_kpis(df: pd.DataFrame) -> list[dict]:
    total_cost = df['집행금액'].sum()
    total_send = df['발송량'].sum()
    total_click = df['클릭수'].sum()
    ctr = calc_ctr_scalar(total_click, total_send)

    months = sorted(df['년월'].unique())
    kpis = [
        {"label": "총 집행금액", "value": compact_num(total_cost, "원"), "delta": "", "delta_type": ""},
        {"label": "총 발송량", "value": compact_num(total_send, "건"), "delta": "", "delta_type": ""},
        {"label": "총 클릭수", "value": compact_num(total_click, "회"), "delta": "", "delta_type": ""},
        {"label": "평균 CTR", "value": f"{ctr:.2f}%", "delta": "", "delta_type": ""},
    ]

    if len(months) >= 2:
        curr_m, prev_m = months[-1], months[-2]
        curr = df[df['년월'] == curr_m]
        prev = df[df['년월'] == prev_m]

        def _d(cv, pv):
            if pv > 0:
                p = (cv - pv) / pv * 100
                s = "+" if p >= 0 else ""
                t = "up" if p > 0 else ("down" if p < 0 else "")
                return f"전월 대비 {s}{p:.1f}%", t
            return "", ""

        for i, col in enumerate(['집행금액', '발송량', '클릭수']):
            d, t = _d(curr[col].sum(), prev[col].sum())
            kpis[i]['delta'] = d
            kpis[i]['delta_type'] = t

        curr_ctr = calc_ctr_scalar(curr['클릭수'].sum(), curr['발송량'].sum())
        prev_ctr = calc_ctr_scalar(prev['클릭수'].sum(), prev['발송량'].sum())
        diff = curr_ctr - prev_ctr
        if abs(diff) > 0.001:
            s = "+" if diff > 0 else ""
            kpis[3]['delta'] = f"전월 대비 {s}{diff:.2f}%p"
            kpis[3]['delta_type'] = "up" if diff > 0 else "down"

    return kpis


def _media_table(df: pd.DataFrame) -> pd.DataFrame:
    m = df.groupby('매체명').agg(
        {'집행금액': 'sum', '발송량': 'sum', '클릭수': 'sum'}
    ).reset_index()
    m['CTR'] = np.where(m['발송량'] > 0, m['클릭수'] / m['발송량'] * 100, 0)
    m['CPC'] = np.where(m['클릭수'] > 0, m['집행금액'] / m['클릭수'], 0)
    return m.sort_values('클릭수', ascending=False)


def _insights(df: pd.DataFrame) -> list[str]:
    items = []
    total = df['발송량'].sum()
    clicks = df['클릭수'].sum()
    ctr = calc_ctr_scalar(clicks, total)
    items.append(f"전체 기간 평균 CTR {ctr:.2f}%, 총 {compact_num(clicks, '회')} 클릭 달성")

    media = _media_table(df)
    media_f = media[media['발송량'] >= 5000]
    if not media_f.empty:
        best = media_f.loc[media_f['CTR'].idxmax()]
        items.append(f"최고 효율 매체: {best['매체명']} (CTR {best['CTR']:.2f}%)")

    months = sorted(df['년월'].unique())
    if len(months) >= 2:
        c = df[df['년월'] == months[-1]]
        p = df[df['년월'] == months[-2]]
        c_ctr = calc_ctr_scalar(c['클릭수'].sum(), c['발송량'].sum())
        p_ctr = calc_ctr_scalar(p['클릭수'].sum(), p['발송량'].sum())
        d = c_ctr - p_ctr
        if abs(d) > 0.01:
            items.append(f"전월 대비 CTR {'상승' if d > 0 else '하락'} {abs(d):.2f}%p ({months[-2]} → {months[-1]})")

    if '요일번호' in df.columns:
        dow = df.groupby('요일번호').agg({'발송량': 'sum', '클릭수': 'sum'}).reset_index()
        dow['CTR'] = np.where(dow['발송량'] > 0, dow['클릭수'] / dow['발송량'] * 100, 0)
        if not dow.empty and dow['CTR'].max() > 0:
            b = dow.loc[dow['CTR'].idxmax()]
            names = {0: '월', 1: '화', 2: '수', 3: '목', 4: '금', 5: '토', 6: '일'}
            items.append(f"요일별 최고 효율: {names.get(int(b['요일번호']), '?')}요일 (CTR {b['CTR']:.2f}%)")

    return items[:6]


def _actions(df: pd.DataFrame) -> list[str]:
    acts = []
    media = _media_table(df)
    media_f = media[media['발송량'] >= 5000]

    if len(media_f) >= 2:
        best = media_f.loc[media_f['CTR'].idxmax()]
        worst = media_f.loc[media_f['CTR'].idxmin()]
        if best['CTR'] > worst['CTR'] * 1.3:
            acts.append(
                f"'{best['매체명']}' 매체 예산 확대 권장 "
                f"(CTR {best['CTR']:.2f}% vs '{worst['매체명']}' {worst['CTR']:.2f}%)"
            )

    if not media_f.empty:
        cpc_pos = media_f[media_f['CPC'] > 0]
        if not cpc_pos.empty:
            low = cpc_pos.loc[cpc_pos['CPC'].idxmin()]
            acts.append(f"CPC 최저 매체: '{low['매체명']}' ({low['CPC']:,.0f}원/클릭) — 비용 효율 극대화 가능")

    months = sorted(df['년월'].unique())
    if len(months) >= 2:
        for _, row in media_f.iterrows():
            nm = row['매체명']
            c = df[(df['년월'] == months[-1]) & (df['매체명'] == nm)]
            p = df[(df['년월'] == months[-2]) & (df['매체명'] == nm)]
            if c['발송량'].sum() >= 5000 and p['발송량'].sum() >= 5000:
                cc = calc_ctr_scalar(c['클릭수'].sum(), c['발송량'].sum())
                pp = calc_ctr_scalar(p['클릭수'].sum(), p['발송량'].sum())
                if pp > 0 and (pp - cc) / pp > 0.2:
                    acts.append(f"'{nm}' CTR 20%+ 하락 — 소재 교체 또는 타겟 재설정 필요")

    if not acts:
        acts.append("현재 매체별 성과가 안정적입니다. 기존 운영 전략을 유지하세요.")
    return acts[:5]


