def generate_monthly_pdf(
    df: pd.DataFrame,
    company_name: str = "",
    service_name: str = "",
) -> bytes:
    """멀티페이지 월간 PDF 리포트 바이트 생성."""
    if df.empty:
        raise ValueError("데이터가 비어 있어 PDF를 생성할 수 없습니다.")

    company = company_name or "LMS Analytics"
    months = sorted(df['년월'].unique())
    period = f"{months[0]} ~ {months[-1]}" if months else "전체 기간"
    gen_date = datetime.now().strftime('%Y년 %m월 %d일 %H:%M')

    pdf = MonthlyPDF(company, period)

    # ── Page 1: 커버 + KPI ──
    pdf.draw_cover(company, period, gen_date)

    pdf.section("Executive Summary")
    kpis = _compute_kpis(df)
    pdf.draw_kpi_cards(kpis)

    # MoM 비교
    if len(months) >= 2:
        pdf.section("MoM 추이", _C['primary'])
        curr_m, prev_m = months[-1], months[-2]
        curr = df[df['년월'] == curr_m]
        prev = df[df['년월'] == prev_m]

        pdf._f('', 7)
        pdf.set_text_color(*_C['text2'])
        pdf.set_x(12)
        pdf.cell(0, 4, f"{prev_m} vs {curr_m}", ln=True)
        pdf.set_y(pdf.get_y() + 1)

        for label, col, unit in [
            ("집행금액", "집행금액", "원"),
            ("발송량", "발송량", "건"),
            ("클릭수", "클릭수", "회"),
        ]:
            pdf.mom_bar(label, curr[col].sum(), prev[col].sum(), unit)

    # ── Page 2: 매체 성과 ──
    pdf.add_page()
    pdf.section("매체별 성과 상세", _C['blue'])

    media = _media_table(df)
    headers = ["매체명", "집행금액", "발송량", "클릭수", "CTR", "CPC"]
    widths = [40, 30, 30, 26, 22, 38]
    rows = []
    for _, r in media.head(15).iterrows():
        rows.append([
            str(r['매체명'])[:12],
            compact_num(r['집행금액'], "원"),
            compact_num(r['발송량'], "건"),
            compact_num(r['클릭수'], "회"),
            f"{r['CTR']:.2f}%",
            f"{r['CPC']:,.0f}원" if r['CPC'] > 0 else "-",
        ])
    pdf.table(headers, rows, widths)

    # 월별 매체 성과 (2개월 이상)
    if len(months) >= 2:
        pdf.section("월별 성과 비교", _C['primary'])
        for m in months[-3:]:  # 최근 3개월
            m_data = df[df['년월'] == m]
            cost = m_data['집행금액'].sum()
            sends = m_data['발송량'].sum()
            clicks = m_data['클릭수'].sum()
            ctr = calc_ctr_scalar(clicks, sends)
            pdf._f('', 8)
            pdf.set_text_color(*_C['text'])
            pdf.set_x(12)
            pdf.cell(0, 5, f"{m}:  {compact_num(cost, '원')}  |  "
                          f"{compact_num(sends, '건')}  |  "
                          f"{compact_num(clicks, '회')}  |  CTR {ctr:.2f}%", ln=True)

    # ── Page 3: 인사이트 + 액션 ──
    pdf.add_page()
    pdf.section("핵심 인사이트", _C['primary'])
    pdf.bullets(_insights(df), _C['primary'])

    pdf.set_y(pdf.get_y() + 3)
    pdf.section("Action Items", _C['blue'])
    pdf.bullets(_actions(df), _C['blue'])

    # 벤치마크 포지셔닝 (Firebase)
    try:
        bench = get_benchmark_stats()
    except Exception:
        bench = None
    if bench and bench['avg_ctr'] > 0:
        pdf.set_y(pdf.get_y() + 3)
        pdf.section("업종 벤치마크 포지셔닝", (108, 99, 255))

        my_send = df['발송량'].sum()
        my_click = df['클릭수'].sum()
        my_cost = df['집행금액'].sum()
        my_ctr = calc_ctr_scalar(my_click, my_send)
        my_cpc = (my_cost / my_click) if my_click > 0 else 0

        # 데이터 규모 캡션
        pdf._f('', 7)
        pdf.set_text_color(*_C['text2'])
        pdf.set_x(12)
        n_ind = len(bench.get('industries', []))
        pdf.cell(0, 4, f"Firebase {bench['total_campaigns']}건 · {n_ind}개 업종 벤치마크 기반", ln=True)
        pdf.set_y(pdf.get_y() + 2)

        # ── 비교 테이블 ──
        ctr_pct = calc_percentile(my_ctr, bench['ctr_values'])
        ctr_grade = percentile_letter(ctr_pct)
        ctr_diff = ((my_ctr - bench['avg_ctr']) / bench['avg_ctr'] * 100) if bench['avg_ctr'] > 0 else 0

        rows_data = [
            ["CTR", f"{my_ctr:.2f}%", f"{bench['avg_ctr']:.2f}%",
             ctr_grade, f"상위 {100 - ctr_pct:.0f}%",
             f"{'+'if ctr_diff >= 0 else ''}{ctr_diff:.1f}%"],
        ]
        if my_cpc > 0 and bench['avg_cpc'] > 0:
            cpc_pct = calc_percentile_lower(my_cpc, bench['cpc_values'])
            cpc_grade = percentile_letter(cpc_pct)
            cpc_diff = ((my_cpc - bench['avg_cpc']) / bench['avg_cpc'] * 100)
            rows_data.append(
                ["CPC", f"{my_cpc:,.0f}원", f"{bench['avg_cpc']:,.0f}원",
                 cpc_grade, f"상위 {100 - cpc_pct:.0f}%",
                 f"{'+'if cpc_diff >= 0 else ''}{cpc_diff:.1f}%"]
            )

        pdf.table(
            ["지표", "자사", "업종 평균", "등급", "백분위", "차이"],
            rows_data,
            [22, 30, 30, 18, 32, 30],
        )

        # ── 종합 등급 박스 ──
        all_pcts = [ctr_pct]
        if my_cpc > 0 and bench['avg_cpc'] > 0:
            all_pcts.append(cpc_pct)
        overall_pct = sum(all_pcts) / len(all_pcts)
        overall_grade = percentile_letter(overall_pct)

        grade_desc = {
            'A': '업종 상위권 — 현재 전략을 유지하며 추가 확장을 고려하세요.',
            'B': '업종 평균 이상 — 효율이 좋은 편이며 소폭 개선 여지가 있습니다.',
            'C': '업종 평균 수준 — 소재·타겟 최적화를 통해 효율을 높일 수 있습니다.',
            'D': '업종 하위권 — 소재 교체, 타겟 재설정, 발송 시간대 변경을 권장합니다.',
        }

        pdf.highlight_box(
            f"종합 등급: {overall_grade} (상위 {100 - overall_pct:.0f}%)  —  "
            f"{grade_desc.get(overall_grade, '')}",
            (108, 99, 255),
        )

    # 마무리 메시지
    pdf.set_y(pdf.get_y() + 5)
    pdf._f('', 8)
    pdf.set_text_color(*_C['text2'])
    pdf.set_x(12)
    pdf.multi_cell(186, 5,
        "본 리포트는 BrainCube LMS Analytics 시스템에서 자동 생성되었습니다. "
        "상세 분석 및 실시간 데이터는 대시보드에서 확인하세요."
    )

    buf = BytesIO()
    pdf.output(buf)
    return buf.getvalue()


