class MonthlyPDF(FPDF):
    """멀티페이지 한글 PDF — 월간 리포트용"""

    def __init__(self, company: str = "", period: str = ""):
        super().__init__(orientation='P', unit='mm', format='A4')
        self._register_font()
        self.set_auto_page_break(auto=True, margin=20)
        self.company = company
        self.period = period

    def _register_font(self):
        for name, regular, bold in _FONTS:
            if os.path.isfile(regular):
                self.add_font(name, '', regular, uni=True)
                if os.path.isfile(bold):
                    self.add_font(name, 'B', bold, uni=True)
                else:
                    self.add_font(name, 'B', regular, uni=True)
                self._ff = name
                return
        self._ff = 'Helvetica'

    def _f(self, style='', size=10):
        self.set_font(self._ff, style, size)

    # ── Header / Footer ──

    def header(self):
        if self.page_no() == 1:
            return  # 커버 페이지는 자체 헤더
        self._f('', 7)
        self.set_text_color(*_C['text2'])
        self.set_xy(12, 8)
        self.cell(0, 4, f"{self.company} | 월간 리포트 | {self.period}", align='L')
        self.set_draw_color(*_C['border'])
        self.line(12, 13, 198, 13)
        self.set_y(16)

    def footer(self):
        self.set_y(-15)
        self.set_draw_color(*_C['border'])
        self.line(12, self.get_y(), 198, self.get_y())
        self._f('', 7)
        self.set_text_color(*_C['text2'])
        self.cell(0, 8, f"Page {self.page_no()} | Generated by BrainCube LMS Analytics", align='C')

    # ── Drawing Helpers ──

    def draw_cover(self, company: str, period: str, gen_date: str):
        """Page 1: 커버"""
        self.add_page()
        # 배경 상단 그라디언트 영역
        self.set_fill_color(*_C['primary'])
        self.rect(0, 0, 210, 100, 'F')

        # 회사명
        self._f('B', 28)
        self.set_text_color(*_C['white'])
        self.set_xy(20, 30)
        self.cell(0, 12, company)

        # 리포트 제목
        self._f('', 14)
        self.set_xy(20, 48)
        self.cell(0, 8, "LMS 월간 성과 리포트")

        # 기간
        self._f('', 11)
        self.set_xy(20, 62)
        self.cell(0, 6, period)

        # 생성일
        self._f('', 9)
        self.set_xy(20, 74)
        self.set_text_color(200, 200, 200)
        self.cell(0, 5, f"Report Generated: {gen_date}")

        # 하단 흰색으로 복귀
        self.set_text_color(*_C['text'])
        self.set_y(110)

    def draw_kpi_cards(self, kpis: list[dict]):
        """KPI 카드 행 (4개)"""
        card_w = (186 - (len(kpis) - 1) * 3) / len(kpis)
        card_h = 24
        y0 = self.get_y()

        for i, kp in enumerate(kpis):
            x = 12 + i * (card_w + 3)
            # 카드 배경
            self.set_fill_color(*_C['card'])
            self.set_draw_color(*_C['border'])
            self.rect(x, y0, card_w, card_h, 'DF', round_corners=True, corner_radius=3)
            # 상단 악센트
            self.set_fill_color(*_C['primary'])
            self.rect(x, y0, card_w, 1.5, 'F')
            # 라벨
            self._f('', 7)
            self.set_text_color(*_C['text2'])
            self.set_xy(x + 3, y0 + 3.5)
            self.cell(card_w - 6, 4, kp['label'])
            # 값
            self._f('B', 14)
            self.set_text_color(*_C['text'])
            self.set_xy(x + 3, y0 + 9)
            self.cell(card_w - 6, 7, kp['value'])
            # 변화율
            if kp.get('delta'):
                self._f('', 7)
                dt = kp.get('delta_type', '')
                if dt == 'up':
                    self.set_text_color(*_C['red'])
                    arrow = chr(0x2191) + ' '
                elif dt == 'down':
                    self.set_text_color(*_C['blue'])
                    arrow = chr(0x2193) + ' '
                else:
                    self.set_text_color(*_C['text2'])
                    arrow = ''
                self.set_xy(x + 3, y0 + 17.5)
                self.cell(card_w - 6, 4, f"{arrow}{kp['delta']}")

        self.set_y(y0 + card_h + 5)

    def section(self, title: str, color: tuple = None):
        """섹션 소제목"""
        c = color or _C['primary']
        y0 = self.get_y()
        if y0 > 260:
            self.add_page()
            y0 = self.get_y()
        self.set_fill_color(*c)
        self.rect(12, y0, 3, 6, 'F')
        self._f('B', 11)
        self.set_text_color(*_C['text'])
        self.set_xy(17, y0)
        self.cell(0, 6, title, ln=True)
        self.set_y(self.get_y() + 2)

    def table(self, headers: list[str], rows: list[list[str]], widths: list[float]):
        """테이블"""
        rh = 6.5
        y0 = self.get_y()
        if y0 + rh * (len(rows) + 1) > 270:
            self.add_page()
            y0 = self.get_y()

        # 헤더
        self.set_fill_color(*_C['bg'])
        self._f('B', 7.5)
        self.set_text_color(*_C['text3'])
        x = 12
        for w, h in zip(widths, headers):
            self.set_xy(x, y0)
            self.cell(w, rh, h, fill=True, align='C')
            x += w
        self.set_y(y0 + rh)

        # 행
        self._f('', 7.5)
        self.set_text_color(*_C['text'])
        for ri, row in enumerate(rows):
            y = self.get_y()
            if y > 270:
                self.add_page()
                y = self.get_y()
            x = 12
            if ri % 2 == 1:
                self.set_fill_color(252, 253, 254)
            else:
                self.set_fill_color(*_C['white'])
            for w, cell in zip(widths, row):
                self.set_xy(x, y)
                a = 'R' if any(c.isdigit() for c in cell) else 'L'
                self.cell(w, rh, cell, fill=True, align=a)
                x += w
            self.set_y(y + rh)

        self.set_draw_color(*_C['border'])
        self.line(12, self.get_y(), 198, self.get_y())
        self.set_y(self.get_y() + 3)

    def bullets(self, items: list[str], color: tuple = None):
        """불릿 포인트"""
        c = color or _C['blue']
        self._f('', 8)
        self.set_text_color(*_C['text'])
        for item in items:
            y = self.get_y()
            if y > 270:
                self.add_page()
                y = self.get_y()
            self.set_fill_color(*c)
            self.ellipse(14, y + 1.5, 2, 2, 'F')
            self.set_xy(18, y)
            self.multi_cell(178, 5, item)
            self.set_y(self.get_y() + 1.5)

    def mom_bar(self, label: str, current: float, previous: float, unit: str = ""):
        """MoM 미니 비교 바"""
        y0 = self.get_y()
        if y0 > 268:
            self.add_page()
            y0 = self.get_y()
        bar_max_w = 60
        max_val = max(current, previous, 1)

        self._f('', 7.5)
        self.set_text_color(*_C['text3'])
        self.set_xy(12, y0)
        self.cell(30, 5, label)

        # 이전
        pw = max((previous / max_val) * bar_max_w, 1)
        self.set_fill_color(220, 225, 230)
        self.rect(42, y0, pw, 4.5, 'F', round_corners=True, corner_radius=1.5)
        self._f('', 6.5)
        self.set_text_color(*_C['text2'])
        self.set_xy(42 + pw + 2, y0)
        self.cell(30, 4.5, compact_num(previous, unit))

        y0 += 5.5
        cw = max((current / max_val) * bar_max_w, 1)
        self.set_fill_color(*_C['primary'])
        self.rect(42, y0, cw, 4.5, 'F', round_corners=True, corner_radius=1.5)
        self._f('B', 6.5)
        self.set_text_color(*_C['text'])
        self.set_xy(42 + cw + 2, y0)
        self.cell(30, 4.5, compact_num(current, unit))

        # 변화율
        if previous > 0:
            change = (current - previous) / previous * 100
            sign = "+" if change >= 0 else ""
            self._f('B', 7.5)
            c = _C['red'] if change > 0 else _C['blue']
            self.set_text_color(*c)
            self.set_xy(140, y0 - 3)
            self.cell(30, 5, f"{sign}{change:.1f}%", align='R')

        self.set_y(y0 + 7)

    def highlight_box(self, text: str, color: tuple = None):
        """강조 박스"""
        c = color or _C['primary']
        y0 = self.get_y()
        if y0 > 262:
            self.add_page()
            y0 = self.get_y()
        # 연한 배경색 (원본 색상에 투명도 대신 밝게 조정)
        self.set_fill_color(
            min(255, c[0] + (255 - c[0]) * 85 // 100),
            min(255, c[1] + (255 - c[1]) * 85 // 100),
            min(255, c[2] + (255 - c[2]) * 85 // 100),
        )
        self.set_draw_color(*c)
        box_h = 12
        self.rect(12, y0, 186, box_h, 'DF', round_corners=True, corner_radius=2)
        self._f('', 8)
        self.set_text_color(*_C['text'])
        self.set_xy(16, y0 + 2)
        self.multi_cell(178, 4.5, text)
        self.set_y(y0 + box_h + 3)


